public class OperatorsArithmetic {
    public static void main(String[] args) {
        //declare variables of type float
        float res, num1, num2;

        //assign values to variables num1, num2
        num1 = 10.0f;
        num2 = 2.0f;

        //store the operation result in varible
        res = num1 + num2;
        System.out.println(res);

        res = num1 - num2;
        System.out.println(res);

        res = num1 * num2;
        System.out.println(res);

        res = num1 / num2;
        System.out.println(res);
    }
}
